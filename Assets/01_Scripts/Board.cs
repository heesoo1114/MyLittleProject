using System.Collections;
using System.Collections.Generic;
using Unity.VisualScripting;
using UnityEditor.Tilemaps;
using UnityEngine;

public class Board : MonoBehaviour
{
    public GameObject blockPrefab;

    int[,] boardInfo = new int[10, 10];

    public int mineCount = 10; // 지뢰개수

    public List<Sprite> spriteList;

    private void Start()
    {
        InitBoard();
        IntantiateBoard();
        DebugBoardInfo();
    }

    [ContextMenu("초기화")]
    public void InitBoard()
    {
        // 2차원 배열 전부 0(기본 땅)으로 초기화
        for (int y = 0; y < 10; y++)
        {
            for (int x = 0; x < 10; x++)
            {
                boardInfo[y, x] = 0;
            }
        }

        // 지뢰 넣어주기
        for (int i = 0; i < mineCount;)
        {
            // 아무 배열 랜덤 지정
            int x = Random.Range(0, 10); // 0 ~ 9 중 랜덤한 값 반환
            int y = Random.Range(0, 10); // 0 ~ 9 중 랜덤한 값 반환

            // 배열이 빈 부분일 때만 -1(지뢰) 넣어주기
            if (boardInfo[y, x] == 0)
            {
                boardInfo[y, x] = -1;
                i++;
            }
        }

        // 기본 땅 주변 지뢰 수 찾고 자신에게 숫자 부여
        int number = 0;
        for (int y = 0; y < 10; y++)
        {
            for (int x = 0; x < 10; x++)
            {
                if (boardInfo[y, x] == -1) continue; // 지뢰일 때

                for (int i = y - 1; i <= y + 1; i++)
                {
                    if (i < 0 || i > 9) continue; // 맵 벗어났을 때

                    for (int j = x - 1; j <= x + 1; j++)
                    {
                        if (j < 0 || j > 9) continue; // 맵 벗어났을 때

                        if (boardInfo[i, j] == -1) // 지뢰일 때
                        {
                            number++;
                        }
                    }
                }

                boardInfo[y, x] = number;
                number = 0;
            }
        }
    }

    [ContextMenu("생성")]
    public void IntantiateBoard()
    {
        for (int y = 0; y < 10; y++)
        {
            for (int x = 0; x < 10; x++)
            {
                GameObject block = Instantiate(blockPrefab, this.gameObject.transform);
                block.name = "Block" + (y * 10 + (x + 1)); // 블록 이름 
                
                Block _block = block.GetComponent<Block>();
                
                _block.blockID = boardInfo[y, x]; // 블록에 정보 넣어줌
                _block.SetBlock();
            }
        }
    }

    [ContextMenu("디버그")]
    public void DebugBoardInfo()
    {
        for (int y = 0; y < 10; y++)
        {
            for (int x = 0; x < 10; x++)
            {
                Debug.Log(boardInfo[y, x]);
            }
        }
    }
}
